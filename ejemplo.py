"""
Calcular la nota de un estudiante, qu tiene 2 sistematicos de 30 y uno de 40
"""

sistematico1=int(input("Ingrese primer sistemático (max: 30): "))
sistematico2=int(input("Ingrese segundo sistemático (max: 30): "))
sistematico3=int(input("Ingrese tercer sistemático (max: 40): "))
nota=(sistematico1+sistematico2+sistematico3)
print("La nota del estudiante es: ", nota)